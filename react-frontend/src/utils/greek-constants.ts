export function initGreekAlphabet() {
    let pipe = document.createElement('div')

    for (let letter in greek2htmlCode) {
        pipe.innerHTML = greek2htmlCode[letter]
        greekAlphabet[letter] = pipe.innerText
    }

    pipe.remove()
}

/**
 *
 * @returns the indexes to form html codes for certain greek characters
 */
export function getSpecialGreekCharsCodes() {
    const avoidListIdx = [7958, 7959, 7966, 7967, 8006, 8007, 8014, 8015, 8024,
        8026, 8028, 8030, 8062, 8063, 8117, 8133, 8148, 8149, 8156, 8176, 8177,
        8181]

    const nonLetters = [8125, 8126, 8127, 8128, 8129, 8141, 8142, 8143, 8157,
        8158, 8159, 8173, 8174, 8175, 8189, 8190]

    const result = []

    for (let i = 7936; i < 8191; i++) {
        if (avoidListIdx.includes(i) || nonLetters.includes(i)) {
            continue
        }

        result.push(i)
    }

    return result
}



